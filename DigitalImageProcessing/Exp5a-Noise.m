I=imread('monkey.jpg');
G=rgb2gray(I);
N1=imnoise(G,'salt & pepper',0.05);
subplot(2,3,1);
imshow(G);
title('GreyScale Image');
MF3=medfilt2(N1,[3 3]);
MF7=medfilt2(N1,[7 7]);
subplot(2,3,2);
imshow(N1);
title('Noise Image');
h=[ 1 1 1 ; 1 1 1 ; 1 1 1]/9;
C=imfilter(G,h);
subplot(2,3,3);
imshow(C);
title('Box Filter Image');
subplot(2,3,4);
imshow(MF3);
title('MF3 Image');
subplot(2,3,5);
imshow(MF7);
title('MF7 Image');
